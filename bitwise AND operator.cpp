#include <stdio.h>  
int main()  
{  
   int a=15,b=13,c;
   c=a&b;
   printf("Bitwise AND operator a&b is %d\n",a&b);  
   c=a|b;
   printf("Bitwise OR operator a|b is %d\n",a|b);
   c=a^b;
   printf("Bitwise exclusive OR operator a^b is %d\n",a^b);
   c=~a;
   printf("one' complement operator ~a is %d\n",~a);
   c=a<<2;
   printf("left shift operator is %d\n",a<<2);
   c=a>>2;
   printf("right shift operator is %d\n",a>>2);
   
   
   
} 























































































































